// Strings com Template Literals
const convite = `Olá ${"Maria"}, você está convidada para a festa no dia ${"25/12"}.`;
console.log(convite);

// Diário de Bordo
const diario = `Data: 12/12/2024\nLocal: Casa\nHoje foi um dia incrível!`;
console.log(diario);

// Redator de Notícias
const noticia = "O evento será em 2023.";
console.log(noticia.replace("2023", "2024"));

// Caixa de Comentários
const comentario = "   Excelente trabalho!   ";
console.log(comentario.trim());

// Carta para um Amigo
const carta = `Olá, amigo!\nEspero que esteja bem.\nNos vemos em breve!\nAbraços.`;
console.log(carta);

// Citação Famosa
const citacao = 'Albert Einstein disse: "A imaginação é mais importante que o conhecimento."';
console.log(citacao);
